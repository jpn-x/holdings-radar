def filer_keyword(filer_name):
    """保有者名からキーワードを抽出（株式会社等除去）"""
    name = re.sub(r'(株式会社|合同会社|有限会社|ホールディングス|ホールデイングス|グループ)', '', filer_name)
    return name.strip()

total_blanked = 0
files_changed = 0

for f in sorted(glob.glob("data/*.json")):
    d = json.load(open(f, encoding="utf-8"))
    modified = False

    for e in d.get("new", []) + d.get("chg", []):
        sec = e.get("sec", "")
        filer = e.get("filer", "")
        name = e.get("name", "")

        for filer_key, wrong_sec in FILER_WRONG.items():
            if filer_key in filer and sec == wrong_sec:
                # 保有者が自社株を保有している正常ケースはスキップ
                # (銘柄名に保有者名のキーワードが含まれる場合)
                kw = filer_keyword(filer_key)
                if kw and len(kw) >= 3 and kw in name:
                    continue
                # 誤コードをクリア
                print(f"  {d['date']} | {sec} -> '' | {name[:20]:20} | filer: {filer[:25]}")
                e["sec"] = ""
                modified = True
                total_blanked += 1
                break

    if modified:
        files_changed += 1
        with open(f, "w", encoding="utf-8") as fp:
            json.dump(d, fp, ensure_ascii=False, indent=2)

print(f"\n計 {total_blanked} 件クリア（{files_changed} ファイル更新）")
